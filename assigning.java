package com.example;

public class assigning {
    public static void main(String[] args) {


        System.out.println(20 > 50);
        System.out.println(999 == 999);
        System.out.println(100 == 20);
        System.out.println(10 > 1000);

        System.out.println(1000 < 1000);
        System.out.println(2 < 2);


        System.out.println(10 > 10);
        System.out.println(0 >= 0);
        System.out.println(748 <= 4878);
        System.out.println(100 <= 100);

        System.out.println(777 <= 100);


        System.out.println(20 == 20);
        System.out.println(10 != 200);
        System.out.println(1 != 1);
        System.out.println(546 == 478);


        System.out.println(500 > 100);
        System.out.println(100 < 500);

    }
}
